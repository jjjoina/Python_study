# 탈출문자

# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")

# 저는 "조인화"입니다.
print("저는 \"조인화\"입니다.") # \"를 써서 "을 문자로 인식

# \\ : 문장 내에서 \
print("C:\\Users\\INHWA CHO\\Desktop\\인화꺼\\Python\\PythonWorkspace>")

# /r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")

# /b : 백스페이스 (한 글자 삭제)
print("Redd\bApple") # 백스페이스로 d 하나 삭제

# \t : 탭
print("Red\tApple")